package javaExamples;

public class Cond_ElseIf {
public static void main(String[]args) {
	int a=20;
	if(a==25) {
		System.out.println("if statement executed");
	}
	else if(a==30) {
		System.out.println("a value is 30");
		
	}
	else if(a==40) {
		System.out.println("a value is 40");
	}
	else {
		System.out.println("else stastement executed");
	}
}
}
